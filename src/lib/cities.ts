export function normalizeCity(value: unknown): string | null {
  if (typeof value !== 'string') return null;
  return cityByNormalizedName.get(normalizePersianLocationText(value)) ?? null;
}

export function normalizeMatchableCity(value: unknown): string | null {
  const city = normalizeCity(value);
  return city && city !== 'سایر شهرهای ایران' ? city : null;
}

export function resolveCityAlias(value: unknown): string | null {
  if (typeof value !== 'string') return null;
  const normalized = normalizePersianLocationText(value)
    .replace(/^(شهرستان|استان|شهر|بخش|دهستان|منطقه)\s+/, '')
    .replace(/\s+(شهرستان|استان)$/, '')
    .trim();
  return normalizeCity(normalized) || CITY_AREA_ALIASES[normalized] || null;
}
